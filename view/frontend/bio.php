<p><em>Jean FORTEROCHE</em>, le 26 février 1956 (acte de naissance), ou en 1958 (selon lui), 
est un écrivain français. Poète, essayiste, romancier et réalisateur, il est, depuis la fin des années 1990,
l'un des auteurs contemporains de langue française 
les plus connus et traduits dans le monde.</p>



<p >

Nationalité : France

Né(e) à : La Réunion , le 26/02/1956 </p>


<p> Il a également fait quelques apparitions remarquées en tant qu'acteur.</p>

<p>Après la publication d'une biographie de Lovecraft, il est révélé par les romans "Extension du domaine de la lutte" et, surtout, "Les particules élémentaires", qui le fait connaître d'un large public.</p>

<p> Ce dernier roman, et son livre suivant "Plateforme", sont considérés comme précurseurs dans la littérature française, notamment pour leur description au scalpel, mais non sans humour, de la misère affective et sexuelle de l'homme occidental dans les années 1990 et 2000.</p>

<p>Avec "La carte et le territoire", <em>Jean FORTEROCHE</em> reçoit le prix Goncourt en 2010, après avoir été plusieurs fois pressenti pour ce prix.</p>

<p>En janvier 2015, il publie un livre où il imagine la France dirigée par un parti musulman : "Soumission".</p>

Il reçoit le prix de la BnF 2015 pour l’ensemble de son œuvre.
</p>

