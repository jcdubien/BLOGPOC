<h1> Jean Forteroche </h1>

<img src="https://upload.wikimedia.org/wikipedia/commons/thumb/4/4d/2008.06.09._Michel_Houellebecq_Fot_Mariusz_Kubik_08.jpg/330px-2008.06.09._Michel_Houellebecq_Fot_Mariusz_Kubik_08.jpg">Jean Forteroche</img>

Michel naît à Saint-Pierre<sup id="cite_ref-2" class="reference"><a href="#cite_note-2"><span class="cite_crochet">[</span>2<span class="cite_crochet">]</span></a></sup>, sur l'île de <a href="/wiki/La_R%C3%A9union" title="La Réunion">La Réunion</a>, fils de René Thomas, guide de haute montagne, et de Lucie Ceccaldi, docteur en médecine, anesthésiste diplômée «&nbsp;major&nbsp;» de la faculté de médecine d'<a href="/wiki/Alger" title="Alger">Alger</a>, tous deux militants communistes. Il est prénommé Michel après une visite de ses parents au <a href="/wiki/Mont-Saint-Michel" class="mw-redirect" title="Mont-Saint-Michel">Mont-Saint-Michel</a><sup id="cite_ref-Noiville_3-0" class="reference"><a href="#cite_note-Noiville-3"><span class="cite_crochet">[</span>3<span class="cite_crochet">]</span></a></sup>. Sa mère aurait falsifié son acte de naissance pour le vieillir de deux ans, car elle le pensait surdoué<sup id="cite_ref-4" class="reference"><a href="#cite_note-4"><span class="cite_crochet">[</span>4<span class="cite_crochet">]</span></a></sup>&nbsp;: il ne serait donc pas né le <span class="nowrap">26 février 1956</span>, comme l'indique son acte de naissance, mais le <span class="nowrap">26 février 1958</span>, selon ses propres déclarations. Ses parents se désintéressent de lui très tôt<sup id="cite_ref-5" class="reference"><a href="#cite_note-5"><span class="cite_crochet">[</span>5<span class="cite_crochet">]</span></a></sup>&nbsp;; le couple se sépare, et une demi-sœur naît peu après. Dans un premier temps, ce sont ses grands-parents maternels, en <a href="/wiki/Alg%C3%A9rie" title="Algérie">Algérie</a>, qui le prennent en charge, puis, après le divorce de ses parents, son père le récupère par un coup de force et le confie à sa grand-mère paternelle, Henriette Thomas, née Houellebecq, également fervente <a href="/wiki/Communiste" class="mw-redirect" title="Communiste">communiste</a>, dont il a adopté le nom de jeune fille comme <a href="/wiki/Pseudonyme" title="Pseudonyme">pseudonyme</a> par reconnaissance<sup id="cite_ref-6" class="reference"><a href="#cite_note-6"><span class="cite_crochet">[</span>6<span class="cite_crochet">]</span></a></sup>.

